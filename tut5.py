# Arithmetic Operators
num1 = 7
num2 = 3
sum = num1 + num2  # Addition

print(num1 + num2)  # Addition
print(sum)

difference = num1 - num2  # Subtraction

print(num1 - num2)  # Subtraction
print(difference)

print(num1 * num2)  # Multiply
print(num1 / num2)  # Division
print(type(num1 / num2))  # Division type
print(num1 % num2)  # Modulus - returns Remainder
print(num1 // num2)  # Quotient
print(type(num1 // num2))  # Quotient type
print(num1 ** num2)  # Exponent ( 7^3 = 7*7*7 )
